import React, { Suspense, useMemo } from "react";
import { Canvas } from "@react-three/fiber";
import {
  Decal,
  Float,
  OrbitControls,
  Preload,
  useTexture,
} from "@react-three/drei";

import CanvasLoader from "../Loader";
// import fallbackIcon from "../assets/fallback.png"; // Add a default fallback image

const Ball = ({ imgUrl }) => {
  const [decal] = useTexture([imgUrl || fallbackIcon]);

  // Handle broken texture edge case
  const isValidTexture = decal?.image?.width > 0;

  if (!isValidTexture) return null;

  return (
    <Float speed={1.75} rotationIntensity={1} floatIntensity={2}>
      <ambientLight intensity={0.25} />
      <directionalLight position={[0, 0, 0.05]} />
      <mesh castShadow receiveShadow scale={2.75}>
        <icosahedronGeometry args={[1, 1]} />
        <meshStandardMaterial
          color="#fff8eb"
          polygonOffset
          polygonOffsetFactor={-5}
          flatShading
        />
        <Decal
          position={[0, 0, 1]}
          rotation={[2 * Math.PI, 0, 6.25]}
          scale={1}
          map={decal}
          flatShading
        />
      </mesh>
    </Float>
  );
};

const BallCanvas = ({ icon }) => {
  // Memoize icon to prevent unnecessary texture reloads
  const memoizedIcon = useMemo(() => icon || fallbackIcon, [icon]);

  return (
    <Canvas
      frameloop="demand"
      dpr={[1, 2]}
      gl={{ preserveDrawingBuffer: true }}
    >
      <Suspense fallback={<CanvasLoader />}>
        <OrbitControls enableZoom={false} />
        <Ball imgUrl={memoizedIcon} />
      </Suspense>
      <Preload all />
    </Canvas>
  );
};

export default BallCanvas;
